package com.learning.model;

public class ServiceCost {
	private boolean serviceCharge;
	private boolean labourCharge;

	public boolean isServiceCharge() {
		return serviceCharge;
	}

	public void setServiceCharge(boolean serviceCharge) {
		this.serviceCharge = serviceCharge;
	}

	public boolean isLabourCharge() {
		return labourCharge;
	}

	public void setLabourCharge(boolean labourCharge) {
		this.labourCharge = labourCharge;
	}
}
